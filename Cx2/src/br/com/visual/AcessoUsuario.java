    private void botaoOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOkActionPerformed

        try {

            if (ftfUsuario.getText() == null || ftfUsuario.getText().isEmpty()) {
                ftfUsuario.requestFocus();
                return;
            }

            if (ftfSenha.getPassword() == null || ftfSenha.getPassword().length == 0) {
                ftfSenha.requestFocus();
                return;
            }

            Integer fuCodigo = Integer.parseInt(ftfUsuario.getText().trim());
            String senha = String.valueOf(ftfSenha.getPassword());
            funcionaBean = controle.acessarUsuario(fuCodigo, senha);
            if (funcionaBean == null || funcionaBean.getFucodigo() == null) {
                JOptionPane.showMessageDialog(this, "Acesso negado!", Constantes.AVISO_SISTEMA, JOptionPane.INFORMATION_MESSAGE);
            }

            //----------------------            
            dispose();

        } catch (Exception ex) {
            Loggin.logDiarioExcecao(ex, true);
        }

    }//GEN-LAST:event_botaoOkActionPerformed

