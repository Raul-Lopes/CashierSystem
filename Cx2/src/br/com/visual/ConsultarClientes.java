    private void obterPadraoPesquisa(String modo) throws IOException {

        String padraoPesquisa;
        Properties arquivoConfig = new Properties();

        if (modo.equalsIgnoreCase("ABERTURA")) {

            arquivoConfig.load(new FileInputStream(new File(System.getProperty("user.dir") + System.getProperty("file.separator") + "config.properties")));

            padraoPesquisa = arquivoConfig.getProperty("pesquisa.cliente");

            if (padraoPesquisa != null && !padraoPesquisa.isEmpty()) {
                comboConsultar.setSelectedIndex(Integer.parseInt(padraoPesquisa));
            }

        } else {

            padraoPesquisa = String.valueOf(comboConsultar.getSelectedIndex());

            arquivoConfig.load(new FileInputStream(new File(System.getProperty("user.dir") + System.getProperty("file.separator") + "config.properties")));
            arquivoConfig.setProperty("pesquisa.cliente", padraoPesquisa);
            FileOutputStream out = new FileOutputStream(new File(System.getProperty("user.dir") + System.getProperty("file.separator") + "config.properties"));
            arquivoConfig.store(out, "");
            out.close();

        }
    }

