private void consultar() throws SQLException, ClassNotFoundException, IOException, Exception {

        jTabbedPane1.setSelectedIndex(0);

        Integer opcaoConsulta = comboConsultar.getSelectedIndex();

        if (ftfPesquisar.getText() == null || ftfPesquisar.getText().isEmpty()) {
            ftfPesquisar.setText("%%");
        }

        switch (opcaoConsulta) {
            case 0: {//nome
                if (jRadioButton1.isSelected()) {
                    listaClienPdvBean = controle.listarClienPdvNome(ftfPesquisar.getText().trim() + "%");
                } else {
                    listaClienPdvBean = controle.listarClienPdvNome("%" + ftfPesquisar.getText().trim() + "%");
                }
            }
            break;
            case 1: {//fantasia
                if (jRadioButton1.isSelected()) {
                    listaClienPdvBean = controle.listarClienPdvFantasia(ftfPesquisar.getText().trim() + "%");
                } else {
                    listaClienPdvBean = controle.listarClienPdvFantasia("%" + ftfPesquisar.getText().trim() + "%");
                }
            }
            break;
            case 2: {//cpf
                if (ftfPesquisar.getText().isEmpty()) {
                    return;
                }
                listaClienPdvBean = controle.listarClienPdvCpf(ftfPesquisar.getText().trim());
            }
            break;
            case 3: {//cgc
                if (ftfPesquisar.getText().isEmpty()) {
                    return;
                }
                listaClienPdvBean = controle.listarClienPdvCgc(ftfPesquisar.getText().trim());
            }
            break;
            case 4: {//codigo interno

                if (ftfPesquisar.getText().isEmpty()) {
                    return;
                }
                listaClienPdvBean = new ArrayList<ClienPdvBean>();
                clienPdvBean = controle.clienPdvClcodigo(Integer.valueOf(ftfPesquisar.getText().trim()));

                if (clienPdvBean != null) {
                    listaClienPdvBean.add(clienPdvBean);
                }
            }
            break;
        }

        //-------------------------------------
        atualizarTabelaItem(listaClienPdvBean);
    }

