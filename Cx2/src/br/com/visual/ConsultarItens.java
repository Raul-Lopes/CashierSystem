    void atualizarTabelaItem(List<MaterPdvBean> listaAuxiliarBean) {

        ((DefaultTableModel) tabelaClientes.getModel()).setRowCount(0);
        if (listaAuxiliarBean != null && listaAuxiliarBean.size() > 0) {

            for (MaterPdvBean o : listaAuxiliarBean) {
                Object[] linha = {
                    o.getMfcodigo(),
                    o.getNome(),
                    o.getUnidadeVO().getUncodigo(),
                    Biblioteca.formatarNumero(Biblioteca.truncar(o.getPrecoven(), 2), "#,###,##0.00"),
                    Biblioteca.formatarNumero(o.getQtdestoq(), "#,###,##0.00")};

                ((DefaultTableModel) tabelaClientes.getModel()).addRow(linha);
            }

            tabelaClientes.setRowSelectionInterval(0, 0); //da pau quando vazia

            botaoSelecionar.setEnabled(true);
            tabelaClientes.requestFocus();

            //rotina abaixo, após consulta o item pelo codigo de barra.
            //já deixa o sistema preparado para próxima consulta automatico,
            //sem que acha intervenção do usuario, desde que a consulta seja por codigo de barra
            //if (Caixa.configuracaoBean.getSegmentoMercado().equalsIgnoreCase("SUPERMERCADO")) {
            if (comboConsultar.getSelectedIndex() == 1) { //codigo barra
                ftfPesquisar.requestFocus();
                //tfPesquisar.selectAll();
                ftfPesquisar.setText("");
            }

        } else {

            ftfPesquisar.requestFocus();
            //tfPesquisar.selectAll();
            ftfPesquisar.setText("");
            botaoSelecionar.setEnabled(false);
        }

    }

