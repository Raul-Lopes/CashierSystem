private void consultar() throws SQLException, ClassNotFoundException, IOException, Exception {

        Integer opcaoConsulta = comboConsultar.getSelectedIndex();
        if (ftfPesquisar.getText() == null || ftfPesquisar.getText().isEmpty()) {
            ftfPesquisar.setText("%%");
        }
        switch (opcaoConsulta) {
            case 0: {//descricao

                if (jRadioButton1.isSelected()) {
                    listaMaterPdvBean = controle.listarMaterPdvNome(ftfPesquisar.getText().trim() + "%");
                } else {
                    listaMaterPdvBean = controle.listarMaterPdvNome("%" + ftfPesquisar.getText().trim() + "%");
                }

            }
            break;
            case 1: {//codigo barra: Mfcodigo
                if (ftfPesquisar.getText().isEmpty()) {
                    return;
                }

                materPdvBean = controle.materPdvMfcodigo(ftfPesquisar.getText().trim());

                listaMaterPdvBean = new ArrayList<>();
                listaMaterPdvBean.add(materPdvBean);
            }
            break;
            case 2: {//referencia
                if (ftfPesquisar.getText().isEmpty()) {
                    return;
                }

                if (jRadioButton1.isSelected()) {
                    listaMaterPdvBean = controle.listarMaterPdvReferencia(ftfPesquisar.getText().trim() + "%");
                } else {
                    listaMaterPdvBean = controle.listarMaterPdvReferencia("%" + ftfPesquisar.getText().trim() + "%");
                }
            }
            break;
            case 3: {//codigo interno
                if (ftfPesquisar.getText().isEmpty()) {
                    return;
                }

                listaMaterPdvBean = new ArrayList<>();
                materPdvBean = controle.materPdvCodIntern(ftfPesquisar.getText().trim());

                if (materPdvBean != null) {
                    listaMaterPdvBean.add(materPdvBean);
                }
            }
            break;
        }

        //-------------------------------------
        atualizarTabelaItem(listaMaterPdvBean);
    }

